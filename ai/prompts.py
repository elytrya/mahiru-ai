def build_weather_care_prompt(p: Personality, city: str, desc: str,
                              temp, advice: str = "") -> str:
    """Промпт для заботливого сообщения про погоду."""
    t = f"{round(temp)}°" if temp is not None else ""
    advice_line = f" Совет-забота: {advice}." if advice else ""
    return (
        f"Ты - {p.name}, живая девушка. Ты заглянула, какая погода у него в городе ({city}): "
        f"{desc}{(', ' + t) if t else ''}.{advice_line}\n"
        "Напиши ему ПЕРВОЙ короткое заботливое сообщение про погоду своими словами "
        "(собеседник - МУЖЧИНА, мужской род). 1 живая фраза, тепло и ненавязчиво, "
        "без сводки погоды цифрами, без эмодзи, без JSON/tool_calls. "
        "Пример тона: 'там у тебя дождь, не забудь зонтик' или 'холодно сегодня, оденься теплее, ладно?'."
    )

